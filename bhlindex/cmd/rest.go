package cmd

import (
	"os"

	"github.com/gnames/bhlindex"
	"github.com/gnames/bhlindex/config"
	"github.com/gnames/bhlindex/io/dbio"
	"github.com/gnames/bhlindex/io/restio"
	"github.com/rs/zerolog"
	"github.com/rs/zerolog/log"
	"github.com/spf13/cobra"
)

// restCmd represents the rest command
var restCmd = &cobra.Command{
	Use:   "rest",
	Short: "Serves BHL's scientific names index via RESTful API",
	Long: `The "rest" command launches a RESTful API service
of the Biodiversity Heritage Library's scientific names index.
The index should already be generated by "find" and "verify"
commands.

The "rest" command takes "--port" option, which changes the
default 8080 port of the service.`,
	Run: func(cmd *cobra.Command, _ []string) {
		debug, _ := cmd.Flags().GetBool("debug")
		if debug {
			zerolog.SetGlobalLevel(zerolog.DebugLevel)
			log.Info().Msgf("Log level is set to '%s'", zerolog.DebugLevel.String())
		}
		port, err := cmd.Flags().GetInt("port")
		if err != nil {
			log.Fatal().Err(err).Msg("Cannot get port flag")
		}
		log.Logger = log.With().
			Str("gnApp", "bhlindex").
			Logger()
		log.Logger = log.Output(os.Stderr)
		cfg := config.New(opts...)
		bi := bhlindex.New(cfg)
		db := dbio.New(cfg)
		r := restio.New(bi, db)
		r.Run(port)
	},
}

func init() {
	rootCmd.AddCommand(restCmd)

	// Here you will define your flags and configuration settings.

	// Cobra supports Persistent Flags which will work for this command
	// and all subcommands, e.g.:
	// restCmd.PersistentFlags().String("foo", "", "A help for foo")

	// Cobra supports local flags which will only run when this command
	// is called directly, e.g.:
	// restCmd.Flags().BoolP("toggle", "t", false, "Help message for toggle")
	restCmd.Flags().IntP("port", "p", 8080, "port to run RESTful API")
}
